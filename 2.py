# Representing matrices using numpy array

import numpy as np

a=np.array(42)
b=np.array([1,2,3,4,5])
c=np.array([[1,2,3],[4,5,6]])
d=np.array([[[1,2,3],[4,5,6]],[[1,2,3],[4,5,6]]])
e=np.array([[[1,2,3],[4,5,6],[1,2,3],[4,5,6]]])

print(a, a.ndim,'\n')
# 42 0
print(b, b.ndim,'\n')
# [1 2 3 4 5] 1
print(c, c.ndim,'\n')
# [[1 2 3]
#  [4 5 6]] 2
print(d, d.ndim,'\n')
# [[[1 2 3]
#   [4 5 6]]

#  [[1 2 3]
#   [4 5 6]]]
print(e, e.ndim,'\n')
# [[[1 2 3]
#   [4 5 6]
#   [1 2 3]
#   [4 5 6]]] 3