# Representing matrices using lists

a=[42]
b=[1,2,3,4,5]
c=[[1,2,3],[4,5,6]]
d=[[[1,2,3],[4,5,6]],[[1,2,3],[4,5,6]]]
e=[[[1,2,3],[4,5,6],[1,2,3],[4,5,6]]]

print(a,'\n')
# [42]
print(b,'\n')
# [1, 2, 3, 4, 5]
print(c,'\n')
# [[1, 2, 3], [4, 5, 6]]
print(d,'\n')
# [[[1, 2, 3], [4, 5, 6]], [[1, 2, 3], [4, 5, 6]]]
print(e,'\n')
# [[[1, 2, 3], [4, 5, 6], [1, 2, 3], [4, 5, 6]]]

