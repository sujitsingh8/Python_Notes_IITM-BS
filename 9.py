# Knowing the data types of the variables

num=10
deci_num=9.5
s='scooby doo!'
l=[10,20,30]

print(num)
print("num is of type: ", type(num)) # <class 'int'>

print(deci_num)
print("deci_num is of type: ", type(deci_num)) # <class 'float'>

print(s)
print("s is of type: ", type(s)) # <class 'str'>

print(l)
print('l is of type: ', type(l)) # <class 'list'>