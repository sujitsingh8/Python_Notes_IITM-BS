# Types of Inheritance in Python

# 1. Single (Simple) Inheritance
'''
One child class inherits from one parent class.
Parent class  →  Child class
'''

# 2. Hierarchical Inheritance
'''
Multiple child classes inherit from the same parent class.
Parent class  →  Child class 1, Child class 2, Child class 3, ...
'''

# 3. Multiple Inheritance
'''
One child class inherits from more than one parent class.
Parent class 1 + Parent class 2  →  Child class
'''

# 4. Multilevel Inheritance
'''
A child class becomes a parent for another class.
Parent class  →  Intermediate class  →  Child class
'''

# 5. Hybrid Inheritance
'''
Combination of two or more types of inheritance
(such as single, multiple, multilevel, hierarchical).
'''