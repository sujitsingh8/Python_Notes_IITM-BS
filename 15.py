# Output of relational operators will always be a boolean value.abs

print(True and True)
# True
print(True and False)
# False
print(False and True)
# False
print(False and False)
# False

print(True or True)
# True
print(True or False)
# True
print(False or True)
# True
print(False or False)
# False

print(not(False))
# False
print(not False) 
# or
print(not(False))
# True