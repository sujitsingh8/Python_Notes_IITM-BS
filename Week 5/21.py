# Three most imp methods related to dictionaries-

d={0:0,1:1,2:4,3:9,4:16}
print(d.keys())
# dict_keys([0, 1, 2, 3, 4])
print(d.values())
# dict_values([0, 1, 4, 9, 16])
print(d.items()) # it prints a list but every element in a list is tuple containing 2 elements are one for key and other for value
# dict_items([(0, 0), (1, 1), (2, 4), (3, 9), (4, 16)])