# Library functions in Python:

import math # it will import whole library of 'math' and we can use any 'function' from it
print(math.log(10))
# 2.302585092994046
print(math.sin(90))
# 0.8939966636005579
print(math.sqrt(4))
# 2.0
print(math.factorial(5))
# 120
print(math.pow(10,3))
# 1000.0