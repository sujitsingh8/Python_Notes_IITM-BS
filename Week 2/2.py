# Escape characters:

# Backslash (\):

# print('It's a beautiful day.') it will be an error

print('It\'s a beautiful day')
# It's a beautiful day
print("It's a beautiful day")
# It's a beautiful day

#...........................................................................................................

# Backslash n (\n):

print('My name is xyz.\nI am from abc.')
# My name is xyz.
# I am from abc.

#...........................................................................................................

# Baclslash t(\t)

print('My name is xyz.I am from abc.')
# My name is xyz.I am from abc.
print('My name is xyz.\t\tI am from abc.')
# My name is xyz.       I am from abc.

#...........................................................................................................

# Triple quotes:

# We can use triple quotes to :
# 1. Store multiple line strings.
# 2. To comment out more than 1 line

z='''first line
second line
third line'''
print(z)
# first line
# second line
# third line