# Knowing about lists

l=[10,20,30.5] # This is how we store a list in a variable

print(l)
# [10,20,30.5]
print(l[1]) # It prints the value at the 0th position in the list
# 10
print(l[1]) # It prints the value at the 1st position in the list
# 20
print('l is of type: ', type(l)) 
# l is of data type: <class 'list'>
print("l[2] is of type: ", type(l[1])) 
# l[2] is of type: <class 'int'>
print("l[2] is of type: ", type(l[2])) 
# l[2] is of type: <class 'float'>